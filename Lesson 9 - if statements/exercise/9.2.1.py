# this will get a name and grade level, and print out a greeting

name = input("What is your name?  ")
grade = int(input("What grade are you in?  "))

if grade == 9:
    print (" hello %s, welcome to high school" %s(name))
elif grade == 10:
    print ("I hope to see you in class %s." %s(name))
elif grade == 11:
    print ("You're almost a senior, %s." %s(name))
elif grade == 12:
    print (" %s, Hope you graduate." %s(name))
else:
    print ("sorry invalid grade.")

    
